# Given the list of grades

grades = [85, 90, 78, 88, 76, 95, 89, 80, 72, 93]

# Sort the grades in descending order and print the sorted list

grades.sort()

print(grades)

# Calculate the average grade and print
# Calculate the sum of all numbers
total_sum = sum(grades)

# Calculate the number of all elements
count = len(grades)

# Divide the sum of all numbers by the number of all elements 

average_grade = total_sum / count

# Print the average
print(f"The average grade is: {average_grade}")

